import my_module
num1 = 10
num2 = 5
sum_result = my_module.add(num1, num2)
print(f"Sum: {sum_result}")
subtract_result = my_module.subtract(num1, num2)
print(f"Difference: {subtract_result}")
multiply_result = my_module.multiply(num1, num2)
print(f"Product: {multiply_result}")
divide_result = my_module.divide(num1, num2)
print(f"Quotient: {divide_result:.2f}")